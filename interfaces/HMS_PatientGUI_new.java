    public void patient_table() {
        Connection dbConn = null;
        try {
            dbConn = dbConnManager.connect();
            pst = dbConn.prepareStatement("select * from patient");
            rs = pst.executeQuery();

            ResultSetMetaData Rsm = rs.getMetaData();

            int c;
            c = Rsm.getColumnCount();

            DefaultTableModel df = (DefaultTableModel) tblpatient.getModel();
            df.setRowCount(0);

            while (rs.next()) {
                Vector v2 = new Vector();

                for (int i = 1; i <= c; i++) {
                    v2.add(rs.getString("patientno"));
                    v2.add(rs.getString("firstname"));
                    v2.add(rs.getString("secondname"));
                    v2.add(rs.getString("nic"));
                    v2.add(rs.getString("age"));
                    v2.add(rs.getString("gender"));
                    v2.add(rs.getString("address"));
                    v2.add(rs.getString("birthday"));
                    v2.add(rs.getString("mstatus"));
                    v2.add(rs.getString("phone"));
                    v2.add(rs.getString("wardno"));
                    v2.add(rs.getString("bedno"));
                    v2.add(rs.getString("blood"));
                    v2.add(rs.getString("description"));
                    v2.add(rs.getString("date"));

                }
                df.addRow(v2);
            }

        } catch (SQLException ex) {
            Logger.getLogger(HMS_PatientGUI_new.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void AutoID() {
        Connection dbConn = null;
        try {

            dbConn = dbConnManager.connect();
            Statement s = dbConn.createStatement();
            rs = s.executeQuery("select MAX(patientno) from patient");
            rs.next();
            rs.getString("MAX(patientno)");

            if (rs.getString("MAX(patientno)") == null) {
                lblpno.setText("PS001");

            } else {
                long id = Long.parseLong(rs.getString("MAX(patientno)").substring(2, rs.getString("MAX(patientno)").length()));
                id++;
                lblpno.setText("PS" + String.format("%03d", id));
            }

        } catch (SQLException ex) {
            Logger.getLogger(HMS_PatientGUI_new.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

