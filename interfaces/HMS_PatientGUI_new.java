    private void txtWard1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtWard1ActionPerformed
        String wardno = txtWard1.getText();
        try {
            Connection dbConn = null;
            String pno = txtpno3.getText();
            dbConn = dbConnManager.connect();
            pst = dbConn.prepareStatement("select count(patient.bedno) FROM patient Where patient.wardno=?");
            pst.setString(1, wardno);
            rs = pst.executeQuery();
            int c = 0;
            if (rs.next()) {
                c = Integer.parseInt(rs.getString(1));
            }

            int availble_beds = beds - c;
            if (availble_beds < 1) {
                lblavailble.setText("please enter another ward ");
                btnAdd.setEnabled(false);
            } else {
                lblavailble.setText("Available");
                btnAdd.setEnabled(true);
            }

        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,ex + "----------ERROR!!!","Warning!", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_txtWard1ActionPerformed

