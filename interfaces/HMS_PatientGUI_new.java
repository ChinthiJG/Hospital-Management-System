    private void btnSearch1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearch1ActionPerformed

        String id = txtpno2.getText();

        HMS_PatientDetails pd = new HMS_PatientDetails();
        HMS_Patient patient = pd.find(id);

        txtFname2.setText(patient.getFirstname());
        txtSecname2.setText(patient.getSecondname());
        txtnic2.setText(patient.getNic());
        
         if (patient.getGender().equals("Male")) {
            comboGender2.setSelectedIndex(0);
        } else  {
            comboGender2.setSelectedIndex(1);
        }
        
        txtage2.setText(patient.getAge());
        String bdate = patient.getBirthday();
        java.util.Date date = null;
        try {
            date = new SimpleDateFormat("yyyy-MM-dd").parse(bdate);
        } catch (ParseException ex) {
            Logger.getLogger(HMS_PatientGUI_new.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtbirthdate2.setDate(date);
      
        

        if (patient.getMstatus().equals("Single")) {
           comboReleationship2.setSelectedIndex(0);
        } else {
            comboReleationship2.setSelectedIndex(1);
        }
         
        
        
        txtAddress2.setText(patient.getAddress());
        txtDescription2.setText(patient.getDescription());
        txtward2.setText(patient.getWardno());
        txtbed2.setText(patient.getBedno());
        
        if (patient.getBloodgroup().equals("A+")) {
           comboBlood2.setSelectedIndex(0);
         }
         else if(patient.getBloodgroup().equals("A-")){
             comboBlood2.setSelectedIndex(1);
         }
         else if(patient.getBloodgroup().equals("B+")){
             comboBlood2.setSelectedIndex(2);
         }
         else if(patient.getBloodgroup().equals("B-")){
             comboBlood2.setSelectedIndex(3);
         }
         else if(patient.getBloodgroup().equals("AB+")){
             comboBlood2.setSelectedIndex(4);
         }
         else if(patient.getBloodgroup().equals("AB-")){
             comboBlood2.setSelectedIndex(5);
         }
         else if(patient.getBloodgroup().equals("O+")){
             comboBlood2.setSelectedIndex(6);
         }
         else if(patient.getBloodgroup().equals("O-")){
             comboBlood2.setSelectedIndex(7);
         }
        
        txtPhone2.setText(patient.getPhone());


    }//GEN-LAST:event_btnSearch1ActionPerformed

